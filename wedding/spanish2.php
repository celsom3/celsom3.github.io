                                <p class="slide_up">M&uacute;sica y el movimiento de los derechos de inmigrantes nos uni&oacute;. Nos conocimos en 2009 mientras luch&aacute;bamos por el DREAM Act durante nuestro tiempo en la Universidad Estatal de Arizona.</p>

                                <p class="slide_up">Ileana ten&iacute;a a Celso completamente enamorado, al punto que el le mandaba poes&iacute;a en vol&uacute;menes. Sin embargo el catalizador de nuestra qu&iacute;mica fue un proyecto involucrando la m&uacute;sica.</p>

                                <p class="slide_up">En nuestros esfuerzos para pasar el DREAM Act, el grupo (antes llamado SUFFRAGE) decidi&oacute; hacer un video para traer a la luz las historias de estudiantes indocumentados en ASU.</p>

                                <p class="slide_up">Alguien alli se dio cuenta que Ileana tiene una bonita voz y que Celso puede tocar la guitarra.</p>

                                <p class="slide_up">Entonces nuestros amigos nos propusieron, “Hey, porque no trabajan en una canci&oacute;n para el video?”</p>

                                <p class="slide_up">Quimica instantanea.</p>

                                <p class="slide_up">Ese fue el principio de una muy bonita amistad. Una amistad que sobrepas&oacute; diferentes tiempos dif&iacute;ciles como la detenci&oacute;n y caso de deportaci&oacute;n de Ileana, y los retos de ser indocumentados en Arizona.</p>

                                <p class="slide_up">Eventualmente, nuestro amor gano. El 23 de Mayo de 2010 pasamos una tarde volando un papalote en el Parque de Indian Steele. Ese dia sabiamos que queria ser mas que amigos y nuestra relaci&oacute;n formal empez&oacute;.</p>

                                <hr>

                                <p class="slide_up">Ambos nos identificamos como Dreamers. No solo por una propuesta de ley, sino porque los dos creemos en la idea de que podemos lograr cualquier sue&ntilde;o que nos propongamos.</p>

                                <p class="slide_up">Esta creencia nos a guiado a lograr muchas metas, como el viajar, mudarnos a Houston, ser independientes, y poder contribuir a nuestras familias.</p>

                                <p class="slide_up">Es este tipo de filosof&iacute;a que sabemos que mantendr&aacute; nuestra relacion unidos y vernos por muchas aventuras o retos, y que vamos a sobrepasarlas juntos, en amor, siempre.</p>
