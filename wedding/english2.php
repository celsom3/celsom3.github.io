                        <h3 class="animated" data-animation="fadeIn" data-animation-delay="100">Our true love story</h3>
                        <div class="row">
                            <div class="col-md-4 text-center animated" data-animation="fadeInLeft" data-animation-delay="100"><img src="img/love-story.png" class="image_resized" alt="">
                            </div>
                            <div class="col-md-8 text-left animated white-text" data-animation="fadeInRight" data-animation-delay="100">
                                <p class="slide_up">Music and the immigrant rights movement brought us together. We met in 2009 while fighting for the DREAM Act during our time at Arizona State University (ASU).</p>

                                <p class="slide_up">Ileana had Celso completely head over heels, to the point where he sent her poetry in volumes. The catalyst for our chemistry, however, was a project involving music.</p>

                                <p class="slide_up">In our efforts to pass the DREAM Act, the group (then called SUFFRAGE) decided to make a video highlighting the stories of undocumented students at ASU.</p>

                                <p class="slide_up">Someone there noticed that Ileana has a beautiful voice and that Celso can play guitar. </p>

                                <p class="slide_up">They said, “Hey, why don’t you work on a song for the video.”</p>

                                <p class="slide_up">Instant chemistry.</p>

                                <p class="slide_up">That was the beginning of a beautiful friendship. A friendship that endured things like Ileana’s deportation case and the struggle of being undocumented in Arizona.</p>

                                <p class="slide_up">Eventually, our love won through. On May 23, 2010, we spent an evening flying a kite at Indian Steele Park. That day, we knew that we wanted to be more than friends, and our formal relationship began.</p>

                                <hr>

                                <p class="slide_up">We both strongly identify ourselves as Dreamers. Not just because of some legislation, but because we both embrace the idea that we can realize any dreams we have.</p>

                                <p class="slide_up">That has lead us to accomplishing many goals we have set for ourselves, such as traveling, moving to Houston, seeking independence, and being able to help our families.</p>

                                <p class="slide_up">It is this type of philosophy that we know will hold our relationship together and see us through many adventures or challenges, and that we will emerge from them together, in love, always.</p>
